#ifndef _COLOR_H_
#define _COLOR_H_

#define NONE					"\e[0m"				//clear the color setting
#define BLACK 					"\e[0;30m"			//black
#define L_BLACK					"\e[1;30m"			//light black
#define RED 					"\e[0;31m"			//red
#define L_RED 					"\e[1;31m"			//light red 
#define GREEN 					"\e[0;32m"			//green
#define L_GREEN 				"\e[1;32m"			//light green
#define YELLOW 					"\e[0;33m"			//yellow
#define L_YELLOW 				"\e[1;33m"			//light yellow
#define BLUE 					"\e[0;34m"			//blue
#define L_BLUE 					"\e[1;34m"			//light blue
#define PURPLE 					"\e[0;35m"			//purple
#define L_PURPLE 				"\e[1;35m"			//light purple
#define CYAN 					"\e[0;36m"			//cyan
#define L_CYAN 					"\e[1;36m"			//light cyan
#define GRAY 					"\e[0;37m"			//gray
#define WHITE 					"\e[1;37m"			//white 
#define BOLD 					"\e[1m"				//white bold
#define UNDERLINE 				"\e[4m"				//Underline, white, normal size
#define BLINK 					"\e[5m"				//Flashing, white, normal size
#define REVERSE 				"\e[7m"				//Reverse, the font background is white and the font is black
#define HIDE 					"\e[8m"				//hide
#define CLEAR 					"\e[2J"				//clear
#define CLRLINE 				"\r\e[K"			//clear line

#endif